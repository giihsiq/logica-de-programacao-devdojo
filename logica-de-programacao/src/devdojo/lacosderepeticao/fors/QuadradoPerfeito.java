package devdojo.lacosderepeticao.fors;

public class QuadradoPerfeito {

	public static void main(String[] args) {

		/*
		 * [PT-BR] O Programa deve exibir os quadrados perfeitos existentes entre 15 a 200.
		 * [ENG] The program should display the perfect squares between 15 and 200.
		 * [ESP] El programa debe mostrar los cuadrados perfectos existentes entre 15 y 200.
		 */
		
		for (int i = 15; i <= 200; i++) {
			System.out.println(i * i);
		}
		
		
	}

}
